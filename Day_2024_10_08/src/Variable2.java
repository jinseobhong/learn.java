
public class Variable2 {

	public static void main(String[] args) {
		// Initialization of a variable : Enter a value at the same time as the declaration
		int a = 100;
		System.out.println("a is :" + a);
		// Variable b is 1 greater than a
		int b = a + 1;
		System.out.println("b is :" + b);
		// i,j is a declaration but k has a value of 34
		// int i,j,k = 34;
		//
		int i;
		int j;
		int k = 34;
		// Value of floating
		double c = 3.14;
		System.out.println("c is :" + c);
	}

}