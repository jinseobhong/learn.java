
public class Variable2 {

	public static void main(String[] args) {
		// Float type
		// float(4byte), 7 valid digits
		// Enter f or F in the last digit to express it
		float f = 3.1415926f;
		System.out.println(f);
		// double(8byte), 15 valid digits
		double d = 3.141592653589793;
		System.out.println(d);
	}
}
