
public class Calc {

	public static void main(String[] args) {
		// Calculator for simple arithmetic operations
		int num1 = 10;
		int num2 = 6;

		// Casting int type data to double type data
		double result = (double) num1 / num2;
		System.out.println(result);
		// The result of integer and integer operations is an integer.
		// The result of calculating a float number is a float number.
		// The result of calculating real numbers and integers is real numbers (it does not matter whether the operation is performed before or after the target).
	}

}
