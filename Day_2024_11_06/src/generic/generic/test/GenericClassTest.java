package generic.generic.test;

import java.util.ArrayList;

public class GenericClassTest {
    public static void main(String[] args) {
        // A generic class formalizes the objects to be handled in a formalized collection, and grammatically checks the class type of the objects that can be put into the collection.
        // // In this example, only the Integer type is defined to be able to handle the collection.
        ArrayList<Integer> numbers = new ArrayList();
        numbers.add(1);
    }
}
